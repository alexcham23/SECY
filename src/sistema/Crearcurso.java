    private void textnumKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textnumKeyTyped
        char c;
        c = evt.getKeyChar();

        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_textnumKeyTyped

